// assignment-operators – assign values:
// = (assign), += (add and assign), -= (subtract and assign), *= (multiply and assign), /= (divide and assign), %= (modulus and assign), **= (exponent and assign)

// comparison-operators – compare values:
// == (equal), === (strict equal), != (not equal), !== (strict not equal), > (greater than), < (less than), >= (greater than or equal), <= (less than or equal)

// logical-operators – combine boolean values:
// && (and), || (or), ! (not)

// bitwise-operators – operate on bits:
// & (and), | (or), ^ (xor), ~ (not), << (left shift), >> (right shift), >>> (unsigned right shift)

// string-operators – combine strings:
// + (concatenation), += (append and assign)

